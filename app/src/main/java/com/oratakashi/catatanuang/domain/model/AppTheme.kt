package com.oratakashi.catatanuang.domain.model

/**
 * Represents the available app theme options.
 *
 * @author oratakashi
 * @since 22 Mar 2026
 */
enum class AppTheme {
    /** Follow the system dark/light mode setting. */
    SYSTEM,

    /** Always use light theme. */
    LIGHT,

    /** Always use dark theme. */
    DARK
}

